-- open the pluggable db
ALTER PLUGGABLE DATABASE ORCLPDB1 OPEN READ WRITE;
ALTER SESSION SET CONTAINER = ORCLPDB1;

--- create tablespaces
CREATE TABLESPACE TS_DATA
DATAFILE '/opt/oracle/oradata/ORCLCDB/ts_data.dbf'
SIZE 100M
AUTOEXTEND ON NEXT 50M
MAXSIZE 10G
EXTENT MANAGEMENT LOCAL
SEGMENT SPACE MANAGEMENT AUTO;

CREATE TABLESPACE TS_IDX
DATAFILE '/opt/oracle/oradata/ORCLCDB/ts_idx.dbf'
SIZE 100M
AUTOEXTEND ON NEXT 50M
MAXSIZE 10G
EXTENT MANAGEMENT LOCAL
SEGMENT SPACE MANAGEMENT AUTO;

--- create user
CREATE USER FIRCOCTY IDENTIFIED BY FIRCOCTY;
GRANT CONNECT TO FIRCOCTY;
GRANT CREATE SESSION TO FIRCOCTY;
GRANT ALL PRIVILEGES TO FIRCOCTY;

--- create user
CREATE USER FIRCOLS IDENTIFIED BY FIRCOLS;
GRANT CONNECT TO FIRCOLS;
GRANT CREATE SESSION TO FIRCOLS;
GRANT ALL PRIVILEGES TO FIRCOLS;

--- commit all changes
COMMIT;
